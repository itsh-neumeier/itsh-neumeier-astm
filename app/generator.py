def digits_only(value: str) -> str:
    return re.sub(r"\D+", "", value or "")


def did_plus(value: str) -> str:
    raw = (value or "").strip().replace(" ", "").replace("-", "").replace("/", "")
    if raw.startswith("+"):
        return "+" + digits_only(raw)
    if raw.startswith("00"):
        return "+" + digits_only(raw[2:])
    if raw.startswith("0") and len(raw) > 1:
        return "+49" + digits_only(raw[1:])
    return "+" + digits_only(raw)


@dataclass
class RenderedConfig:
    pjsip: str
    extensions: str
    cdr: str
    cdr_csv: str
    logger: str
    unifi_summary: str
    security_notes: str


def load_state(store: Any) -> dict[str, Any]:
    settings = store.get_settings()
    numbers = [dict(row) for row in store.list_rows("numbers")]
    clients = [dict(row) for row in store.list_rows("clients")]
    routes_inbound = [dict(row) for row in store.list_rows("routes_inbound")]
    routes_outbound = [dict(row) for row in store.list_rows("routes_outbound")]
    return {
        "settings": settings,
        "numbers": numbers,
        "clients": clients,
        "routes_inbound": routes_inbound,
        "routes_outbound": routes_outbound,
    }


def render(store: Any) -> RenderedConfig:
    state = load_state(store)
    return RenderedConfig(
        pjsip=render_pjsip(state),
        extensions=render_extensions(state),
        cdr=render_cdr(),
        cdr_csv=render_cdr_csv(),
        logger=render_logger(),
        unifi_summary=render_unifi_summary(state),
        security_notes=render_security_notes(state),
    )


