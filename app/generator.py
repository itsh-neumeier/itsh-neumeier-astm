def apply_config(store: Any, reload_asterisk: bool = True) -> dict[str, str]:
    rendered = render(store)
    ASTERISK_ETC.mkdir(parents=True, exist_ok=True)
    BACKUP_DIR.mkdir(parents=True, exist_ok=True)
    stamp = datetime.now().strftime("%Y%m%d-%H%M%S")
    backup_path = BACKUP_DIR / stamp
    backup_path.mkdir(parents=True, exist_ok=True)
    for filename in ("pjsip.conf", "extensions.conf", "cdr.conf", "cdr_csv.conf", "logger.conf"):
        source = ASTERISK_ETC / filename
        if source.exists():
            shutil.copy2(source, backup_path / filename)
    (ASTERISK_ETC / "pjsip.conf").write_text(rendered.pjsip, encoding="utf-8")
    (ASTERISK_ETC / "extensions.conf").write_text(rendered.extensions, encoding="utf-8")
    (ASTERISK_ETC / "cdr.conf").write_text(rendered.cdr, encoding="utf-8")
    (ASTERISK_ETC / "cdr_csv.conf").write_text(rendered.cdr_csv, encoding="utf-8")
    (ASTERISK_ETC / "logger.conf").write_text(rendered.logger, encoding="utf-8")
    (BACKUP_DIR / "unifi-talk-config-summary.txt").write_text(
        rendered.unifi_summary, encoding="utf-8"
    )
    (BACKUP_DIR / "itsh-astm-security-notes.txt").write_text(
        rendered.security_notes, encoding="utf-8"
    )
    reload_status = "skipped"
    if reload_asterisk:
        reload_status = reload()
    return {"backup": str(backup_path), "reload": reload_status}


def reload() -> str:
    commands = ["pjsip reload", "dialplan reload", "cdr reload", "logger reload"]
    outputs: list[str] = []
    for command in commands:
        try:
            result = subprocess.run(
                ["asterisk", "-rx", command],
                check=False,
                capture_output=True,
                text=True,
                timeout=15,
            )
            outputs.append((result.stdout or result.stderr or "").strip())
        except Exception as exc:
            outputs.append(f"{command}: {exc}")
    return "\n".join(output for output in outputs if output) or "ok"


def asterisk_status() -> str:
    try:
        result = subprocess.run(
            ["asterisk", "-rx", "pjsip show endpoints"],
            check=False,
            capture_output=True,
            text=True,
            timeout=15,
        )
        return (result.stdout or result.stderr or "").strip()
    except Exception as exc:
        return f"Asterisk status unavailable: {exc}"
